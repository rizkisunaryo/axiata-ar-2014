<div style="margin-top:20px;" class="title-orange" data-sr="enter bottom, move 20px">M-Insurance in our Markets</div>
<div style="margin-top:10px;width:510px;" class="justify" data-sr="enter bottom, move 20px">
In Bangladesh, Robi has launched an innovative service to offer free life insurance coverage for all of its prepaid subscribers. This is a joint effort between Bima and Robi to create value added services for mobile subscribers, who would otherwise have no access to life insurance. The life insurance policy is a cumulative life insurance package offered to all Robi prepaid customers. Each registered subscriber is entitled to his or her insurance coverage every calendar month depending on his or her airtime usage. For approximately 78% of the subscribers, this has been the first time that they have had access to insurance. As of December 2014, Robi has over 6 million registered subscribers under ‘Robi Bima Mobile Insurance.  Since 2012, there have been 504 claims under the Insurance coverage against which BDT20.6 million have been settled so far.
<br><br>
In Indonesia, XL has provided medical, holiday and other forms of insurance to its customers. It is an easy to use application where customers are required to use their mobile devices to register for the type of service they require. Benefits are dispensed speedily with the minimum of paperwork. Free life insurance is provided to post-paid customers with the benefit linked to the customer’s monthly usage. Registration for all these services is a matter of minutes. 1.5 million customers are currently registered to this insurance service.
<br><br>
In Sri Lanka, Dialog provides accident cover to subscribers for under US10 cents a day. The Accident Cover will provide insurance benefits in the event of death or disability. It is a safety net in times of need and helps the family cover costs that occur if the insured suffers a serious accident or passes away.
