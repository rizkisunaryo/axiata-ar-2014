<div style="margin-top:10px;" class="justify" data-sr="enter bottom, move 20px">
Research has shown that more than 1.7 billion people accessed social media in 2014. This number correlates closely with the increase in smartphone penetration. In our markets however, the average smartphone penetration is less than 50%. In light of this, Bangladesh and Malaysia launched Circle and Kolony, allowing users to access social media via feature phones.
</div>

<div style="margin-top:20px;" class="title-orange" data-sr="enter bottom, move 20px">Access to Social Media in Bangladesh and Malaysia</div>
<div style="margin-top:10px;" class="justify" data-sr="enter bottom, move 20px">
Robi’s Circle, unlike web-centric social networks such as Facebook, was designed to be truly mobile-centric with SMS at its core. This liberates the experience from a web connection and makes it available to anyone with even the most basic mobile phone. By end of 2014, there were 226,189 active subscribers of ‘Circle’.<br><br>

In Malaysia, Celcom supports the largest SMS based social networking platform called ‘Kolony’. Celcom’s customers can now connect with their peeps or make new ones every day through their mobile phones. This new way of communicating with social networks has enhanced the overall customer experience. More than five million users are currently registered in the Kolony network.
</div>

<div style="margin-top:20px;" class="title-orange" data-sr="enter bottom, move 20px">XL - FB</div>
<div style="margin-top:10px;" class="justify" data-sr="enter bottom, move 20px">
XL Axiata partnered with Facebook and Ericsson (NASDAQ: ERIC) to find a new methodology to measure and improve end-to-end network performance using simulated Facebook application use cases last year. This is part of Internet.org’s mission to bring affordable internet access to two-thirds of the world not yet connected. With the methodology set forth in this initiative, a model will be developed to improve network performance.
<br><br>
XL and these global giants work closely to bring the best mobile internet experience for customers in Indonesia.
</div>

<div style="margin-top:20px;" class="title-orange" data-sr="enter bottom, move 20px">Xmart Village</div>
<div style="margin-top:10px;" class="justify" data-sr="enter bottom, move 20px">
In Indonesia the Xmart Village was launched in January 2014. It was initiated in Dusun Ibun, and Garut Regency in partnership with the Bandung Institute of Technology. Xmart Village aims to identify the problems of those living in rural and remote locations and provide innovative solutions for these problems. It starts by identifying the needs and challenges faced by particular communities and then using technology to help respond to these challenges.
<br><br>
A number of innovative solutions have been provided by XL through this platform which includes:
<ul style="padding-left: 15px;">
<li>‘Mobile Advertising’ enabling users to get information on recreation through SMS.</li>
<li>‘Mobile Farmers’ which enables users to obtain information on weather in an area through an SMS.</li>
<li>‘Musikkamu’ which enables users to download music.</li>
<li>‘Mobile Banking’ which provides access to financial services through a mobile device.</li>
<li>AMR which enables subscribers to top up electric power through their mobiles.</li>
<li>‘Mobile Xurveillance’ which helps monitor surrounding areas with a surveillance camera.</li>
</ul>

<br>
Through Xmart Village, XL has empowered 43,000 villages across Indonesia and helped them enjoy the many benefits of a digital lifestyle. Xmart Village seeks to introduce innovative, creative and effective solutions for those in rural and under-served communities.
</div>

<div style="margin-top:20px;" class="title-orange" data-sr="enter bottom, move 20px">Ideamart<img src="images/m-money-emblem.png" width="75px" height="69px"/></div>
<div style="margin-top:10px;" class="justify" data-sr="enter bottom, move 20px">
In Sri Lanka Ideamart was developed as a platform by Dialog to help App developers and SME’s to use the Dialog network features through API’s (Application Programme Interface) and monetise such applications. It enables those with great ideas but little ‘know how’ to translate those ideas into marketable Apps, by using the functionalities, sample codes and tutorials in the platform. Over the last 2 years, Ideamart has evolved beyond a platform to an ecosystem which comprises developer community, service providers, team, entrepreneurs, venture capitalists and other support services companies.
<br><br>
Currently the Ideamart ecosystem sustains more than 850,000 subscribers, more than 100 entrepreneurs and almost 2,200 active developers which grows at an average 18% monthly. The Developers Ideamart can use Dialog’s App store or Allapps.lk to publish their applications. All the applications in Ideamart is monetized and the latest improvement Android In-App SDK will allow developers to create Android Apps and monetize as well. Ideamart was awarded as the Best Technology Enabler at the GSMA Mobile World Congress in 2015.
