<div style="margin-top:20px;" class="title-orange" data-sr="enter bottom, move 20px">XL Tunai</div>
<div style="margin-top:10px;width:680px;display: inline-block;" class="justify" data-sr="enter bottom, move 20px">
XL Tunai is our mobile money service in Indonesia that allows customers to perform a range of financial transactions through their mobile phones. The service facilitates a variety of services including the purchase of credit towards their bills, payments to merchants, and cash transfers locally and globally. At the end of 2014 there were 1.3 million registered users and the total transaction volume for the year was 2.5 million. XL Tunai is guaranteed by Otoritas Jasa Keuangan the financial services authority Indonesia and this provides additional security to XL customers who wish to use this platform.</div>
