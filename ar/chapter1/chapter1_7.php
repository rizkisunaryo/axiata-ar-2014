		var strategicBars = [];

	  $(function () {
			strategicBarTrigger1Top = $('#strategicBarTrigger1').offset().top;
			strategicBarTrigger2Top = $('#strategicBarTrigger2').offset().top;
			strategicBarHolder = $('#strategicBarHolder');
			for (var i=0; i<=5; i++) {
				strategicBars[i] = $('#strategicBar'+i);
			};

			// SCROLL MAGIC
			var controller = new ScrollMagic.Controller();
			new ScrollMagic.Scene({triggerElement: "#ceo1BgDiv",triggerHook:"onLeave"})
									.setPin("#ceo1BgDiv")
									.addTo(controller);

			// OTHERS
			animateStrategicBars();
			$(window).on('scroll', function(){
				animateStrategicBars();
			});
		});

		function animateStrategicBars() {
			var scrollTrigger100Top = $('#scrollTrigger100').offset().top;
			var yFactor = scrollTrigger100Top-strategicBarTrigger1Top;
			if (yFactor>151) yFactor=151;
			
			if (scrollTrigger100Top<strategicBarTrigger1Top) {
				strategicBarHolder.css('left','-330px');
				strategicBarHolder.css('opacity','0');
				for (var i=0; i<=5; i++) {
					strategicBars[i].css('left','0');
				};
			} else if (scrollTrigger100Top>strategicBarTrigger2Top) {
				strategicBarHolder.css('left','0px');
				strategicBarHolder.css('opacity','1');
				for (var i=1; i<=5; i++) {
					strategicBars[i].css('left',(i*151)+'px');
				};
			} else{
				strategicBarHolder.css('left',(yFactor-151)+'px');
				strategicBarHolder.css('opacity',(yFactor/151.0));
				for (var i=1; i<=5; i++) {
					strategicBars[i].css('left',(i*yFactor)+'px');
				};
			};
		}
		</script>